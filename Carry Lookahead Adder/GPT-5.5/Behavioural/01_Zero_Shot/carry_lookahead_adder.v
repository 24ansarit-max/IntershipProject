`timescale 1ns/1ps 
 
module cla_16bit_behavioral 
#( 
    parameter WIDTH = 16 
) 
( 
    input  wire [WIDTH-1:0] a, 
    input  wire [WIDTH-1:0] b, 
    input  wire             cin, 
 
    output reg  [WIDTH-1:0] sum, 
    output reg              cout, 
    output reg              overflow 
); 
 
    // Internal Generate, Propagate, Carry 
    reg [WIDTH-1:0] g; 
    reg [WIDTH-1:0] p; 
    reg [WIDTH:0]   c; 
 
    integer i; 
 
    always @(*) begin 
 
        //-------------------------------------------------- 
        // Generate and Propagate Signals 
        //-------------------------------------------------- 
 
        for(i = 0; i < WIDTH; i = i + 1) begin 
            g[i] = a[i] & b[i]; 
            p[i] = a[i] ^ b[i]; 
        end 
 
        //-------------------------------------------------- 
        // Initial Carry 
        //-------------------------------------------------- 
 
        c[0] = cin; 
 
        //-------------------------------------------------- 
        // Group 0 : Bits [3:0] 
        //-------------------------------------------------- 
 
        c[1] = g[0] | 
               (p[0] & c[0]); 
 
        c[2] = g[1] | 
               (p[1] & g[0]) | 
               (p[1] & p[0] & c[0]); 
 
        c[3] = g[2] | 
               (p[2] & g[1]) | 
               (p[2] & p[1] & g[0]) | 
               (p[2] & p[1] & p[0] & c[0]); 
 
        c[4] = g[3] | 
               (p[3] & g[2]) | 
               (p[3] & p[2] & g[1]) | 
               (p[3] & p[2] & p[1] & g[0]) | 
               (p[3] & p[2] & p[1] & p[0] & c[0]); 
 
        //-------------------------------------------------- 
        // Group 1 : Bits [7:4] 
        //-------------------------------------------------- 
 
        c[5] = g[4] | 
               (p[4] & c[4]); 
 
        c[6] = g[5] | 
               (p[5] & g[4]) | 
               (p[5] & p[4] & c[4]); 
 
        c[7] = g[6] | 
               (p[6] & g[5]) | 
               (p[6] & p[5] & g[4]) | 
               (p[6] & p[5] & p[4] & c[4]); 
 
        c[8] = g[7] | 
               (p[7] & g[6]) | 
               (p[7] & p[6] & g[5]) | 
               (p[7] & p[6] & p[5] & g[4]) | 
               (p[7] & p[6] & p[5] & p[4] & c[4]); 
 
        //-------------------------------------------------- 
        // Group 2 : Bits [11:8] 
        //-------------------------------------------------- 
 
        c[9] = g[8] | 
               (p[8] & c[8]); 
 
        c[10] = g[9] | 
                (p[9] & g[8]) | 
                (p[9] & p[8] & c[8]); 
 
        c[11] = g[10] | 
                (p[10] & g[9]) | 
                (p[10] & p[9] & g[8]) | 
                (p[10] & p[9] & p[8] & c[8]); 
 
        c[12] = g[11] | 
                (p[11] & g[10]) | 
                (p[11] & p[10] & g[9]) | 
                (p[11] & p[10] & p[9] & g[8]) | 
                (p[11] & p[10] & p[9] & p[8] & c[8]); 
 
        //-------------------------------------------------- 
        // Group 3 : Bits [15:12] 
        //-------------------------------------------------- 
 
        c[13] = g[12] | 
                (p[12] & c[12]); 
 
        c[14] = g[13] | 
                (p[13] & g[12]) | 
                (p[13] & p[12] & c[12]); 
 
        c[15] = g[14] | 
                (p[14] & g[13]) | 
                (p[14] & p[13] & g[12]) | 
                (p[14] & p[13] & p[12] & c[12]); 
 
        c[16] = g[15] | 
                (p[15] & g[14]) | 
                (p[15] & p[14] & g[13]) | 
                (p[15] & p[14] & p[13] & g[12]) | 
                (p[15] & p[14] & p[13] & p[12] & c[12]); 
 
        //-------------------------------------------------- 
        // Sum Generation 
        //-------------------------------------------------- 
 
        for(i = 0; i < WIDTH; i = i + 1) 
            sum[i] = p[i] ^ c[i]; 
 
        //-------------------------------------------------- 
        // Final Carry Out 
        //-------------------------------------------------- 
 
        cout = c[16]; 
 
        //-------------------------------------------------- 
        // Signed Overflow Detection 
        // Overflow = Carry into MSB XOR Carry out of MSB 
        //-------------------------------------------------- 
 
        overflow = c[15] ^ c[16]; 
 
    end 
 
    // Behavioral model — not direct gate mapping 
    // Power: depends on synthesis tool mapping 
    // Area: inferred from RTL, tool-dependent 
    // Delay: ~log(N) carry chain 
 
endmodule
