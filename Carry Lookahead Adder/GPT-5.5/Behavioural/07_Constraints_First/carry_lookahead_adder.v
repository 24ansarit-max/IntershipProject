module cla16_behavioral ( 
    input  [15:0] a, 
    input  [15:0] b, 
    input         cin, 
    output reg [15:0] sum, 
    output reg        cout, 
    output reg        overflow 
); 
 
    //========================================================= 
    // TIMING_ACHIEVED: 
    // Carry path depth ≈ 4 logic levels 
    // Estimated delay ≈ 2.0 ns @ 28nm 
    //========================================================= 
 
    integer i; 
 
    reg [15:0] g; 
    reg [15:0] p; 
 
    reg [3:0] gp; 
    reg [3:0] gg; 
 
    (* keep = "true" *) reg [16:0] c; 
 
    always @(*) begin 
 
        //----------------------------------------------------- 
        // Bit Generate / Propagate 
        //----------------------------------------------------- 
        for(i=0;i<16;i=i+1) begin 
            g[i] = a[i] & b[i]; 
            p[i] = a[i] ^ b[i]; 
        end 
 
        // SYNTH NOTE: 
        // Maps to AND/XOR bank 
        // ~1 logic level 
 
        //----------------------------------------------------- 
        // Group 0 
        //----------------------------------------------------- 
        gp[0] = p[3] & p[2] & p[1] & p[0]; 
 
        gg[0] = g[3] | 
                (p[3] & g[2]) | 
                (p[3] & p[2] & g[1]) | 
                (p[3] & p[2] & p[1] & g[0]); 
 
        //----------------------------------------------------- 
        // Group 1 
        //----------------------------------------------------- 
        gp[1] = p[7] & p[6] & p[5] & p[4]; 
 
        gg[1] = g[7] | 
                (p[7] & g[6]) | 
                (p[7] & p[6] & g[5]) | 
                (p[7] & p[6] & p[5] & g[4]); 
 
        //----------------------------------------------------- 
        // Group 2 
        //----------------------------------------------------- 
        gp[2] = p[11] & p[10] & p[9] & p[8]; 
 
        gg[2] = g[11] | 
                (p[11] & g[10]) | 
                (p[11] & p[10] & g[9]) | 
                (p[11] & p[10] & p[9] & g[8]); 
 
        //----------------------------------------------------- 
        // Group 3 
        //----------------------------------------------------- 
        gp[3] = p[15] & p[14] & p[13] & p[12]; 
 
        gg[3] = g[15] | 
                (p[15] & g[14]) | 
                (p[15] & p[14] & g[13]) | 
                (p[15] & p[14] & p[13] & g[12]); 
 
        // SYNTH NOTE: 
        // Maps to AND/OR reduction trees 
        // ~2 logic levels 
 
        //----------------------------------------------------- 
        // Carry Initialization 
        //----------------------------------------------------- 
        c[0] = cin; 
 
        //----------------------------------------------------- 
        // Inter-Group Carry Lookahead 
        //----------------------------------------------------- 
        c[4]  = gg[0] | (gp[0] & c[0]); 
        c[8]  = gg[1] | (gp[1] & c[4]); 
        c[12] = gg[2] | (gp[2] & c[8]); 
        c[16] = gg[3] | (gp[3] & c[12]); 
 
        // SYNTH NOTE: 
        // Maps to OR-AND CLA tree 
        // ~1 logic level 
 
        //----------------------------------------------------- 
        // Intra-Group Carries 
        //----------------------------------------------------- 
        c[1] = g[0]  | (p[0]  & c[0]); 
        c[2] = g[1]  | (p[1]  & c[1]); 
        c[3] = g[2]  | (p[2]  & c[2]); 
 
        c[5] = g[4]  | (p[4]  & c[4]); 
        c[6] = g[5]  | (p[5]  & c[5]); 
        c[7] = g[6]  | (p[6]  & c[6]); 
 
        c[9]  = g[8]  | (p[8]  & c[8]); 
        c[10] = g[9]  | (p[9]  & c[9]); 
        c[11] = g[10] | (p[10] & c[10]); 
 
        c[13] = g[12] | (p[12] & c[12]); 
        c[14] = g[13] | (p[13] & c[13]); 
        c[15] = g[14] | (p[14] & c[14]); 
 
        // SYNTH NOTE: 
        // Local carry generation 
        // ~1 logic level inside each group 
 
        //----------------------------------------------------- 
        // Sum Generation 
        //----------------------------------------------------- 
        for(i=0;i<16;i=i+1) 
            sum[i] = p[i] ^ c[i]; 
 
        // SYNTH NOTE: 
        // XOR network 
        // ~1 logic level 
 
        //----------------------------------------------------- 
        // Outputs 
        //----------------------------------------------------- 
        cout = c[16]; 
 
        //----------------------------------------------------- 
        // Signed Overflow 
        //----------------------------------------------------- 
        overflow = c[15] ^ c[16]; 
 
    end 
 
